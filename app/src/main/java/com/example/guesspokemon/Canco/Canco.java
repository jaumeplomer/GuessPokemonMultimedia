package com.example.guesspokemon.Canco;

//POJO
public class Canco {

    public long id;
    public String nom;
    public int preferencia;

    public Canco() {
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public int getPreferencia() {
        return preferencia;
    }

    public void setPreferencia(int preferencia) {
        this.preferencia = 1;
    }
}
