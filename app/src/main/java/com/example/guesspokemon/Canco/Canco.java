package com.example.guesspokemon.Canco;

//POJO
public class Canco {

    public long id;
    public String nom;
    public byte[] canco;

    public Canco() {
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public byte[] getCanco() {
        return canco;
    }

    public void setCanco(byte[] canco) {
        this.canco = canco;
    }
}
